// Numbers:
// Byte    [-128, 127]
// Short   [-32768, 32767]
// Int     [-2,147,483,648 (-2^31), 2,147,483,647 (2^31-1)]
// Long    [-2^63, 2^63-1]
// Float
// Double

val one = 1 // Int
val threeBillion = 3000000000 // Long
val oneLong = 1L // Long
val oneByte: Byte = 1 // Byte
val pi = 3.14 // Double
val one: Double = 1 // Error: type mismatch
val oneDouble = 1.0 // Double
val piDouble = 3.14159265 // Double
val piFloat = 3.14159265f // Float, actual value is 3.1415927


// Booleans:
// The type Boolean can have two values: true and false

val myTrue: Boolean = true
val myFalse: Boolean = false


// Strings:
// A string value is a sequence of characters in double quotes: "abcd"

val name = "Mohammad"


// Characters:
// Character literals go in single quotes: '1'

val aChar: Char = 'a'



